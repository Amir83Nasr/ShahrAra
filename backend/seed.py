def generate_random_national_id() -> str:
    return str(random.randint(1000000000, 9999999999))


def seed():
    db = SessionLocal()

    # Clear existing data
    db.query(Request).delete()
    db.query(User).delete()

    # Create admin user
    admin_user = User(
        phone=ADMIN_PHONE,
        national_id=ADMIN_NATIONAL_ID,
        first_name="مدیر",
        last_name="سامانه",
        is_admin=True,
    )
    db.add(admin_user)
    # Assign random national IDs for users
    for u in USERS:
        u["national_id"] = generate_random_national_id()
        db.add(User(**u))
    db.commit()
    print(f"Seeded {len(USERS) + 1} users (1 admin + {len(USERS)} citizens)")

    all_users = db.query(User).filter(User.is_admin.is_(False)).all()

    base_time = datetime.now(timezone.utc) - timedelta(days=60)

    request_count = 60

    from sqlalchemy import text

    for i in range(request_count):
        user = random.choice(all_users)
        is_problem = random.random() < 0.65
        category = random.choice(CATEGORIES)
        region = random.choice(REGIONS)
        status = random.choice(STATUSES)
        lat = round(random.uniform(34.620, 34.680), 4)
        lng = round(random.uniform(50.820, 50.990), 4)
        likes = random.randint(0, 60)
        created = base_time + timedelta(
            days=random.randint(0, 58),
            hours=random.randint(0, 23),
            minutes=random.randint(0, 59),
        )

        if is_problem:
            title = random.choice(PROBLEM_TITLES)
            description = random.choice(PROBLEM_DESCRIPTIONS)
        else:
            title = random.choice(IDEA_TITLES)
            description = random.choice(IDEA_DESCRIPTIONS)

        admin_response = None
        if status in ("resolved", "archived"):
            admin_responses = [
                "درخواست بررسی و اقدام لازم انجام شد.",
                "تیم مربوطه به محل اعزام و مشکل برطرف شد.",
                "این درخواست در برنامه عملیاتی شهرداری قرار گرفت و به اتمام رسید.",
                "پس از بررسی کارشناسی، اقدامات لازم صورت پذیرفت.",
                "با همکاری شهروندان محترم، این مشکل مرتفع گردید.",
            ]
            admin_response = random.choice(admin_responses)

        db.execute(
            text("""
                INSERT INTO requests (id, title, description, type, category, lat, lng,
                    region, status, user_phone, user_name, created_at, admin_response, likes)
                VALUES (:id, :title, :description, :type, :category, :lat, :lng,
                    :region, :status, :user_phone, :user_name, :created_at, :admin_response, :likes)
            """),
            {
                "id": f"req_{uuid.uuid4().hex[:8]}",
                "title": title,
                "description": description,
                "type": "problem" if is_problem else "idea",
                "category": category,
                "lat": str(lat),
                "lng": str(lng),
                "region": region,
                "status": status,
                "user_phone": user.phone,
                "user_name": f"{user.first_name} {user.last_name}",
                "created_at": created,
                "admin_response": admin_response,
                "likes": likes,
            },
        )

    db.commit()
    db.close()
    print(
        f"Seeded {request_count} requests across {len(CATEGORIES)} categories and {len(STATUSES)} statuses"
    )


if __name__ == "__main__":
    seed()
